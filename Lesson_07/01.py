# write a map function that squares a list of numbers

def w(aa):
    return aa + aa
c = map(w ,range(10))
print(list(c))

# write a filter function that returns only the even numbers from a list of numbers

def w1(ab):
    return ab % 2 == 0
c = filter(w1 ,range(10))
print(list(c))

